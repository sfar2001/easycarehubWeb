/** Reset text cursor to the left margin and a given y. */
function moveTo(doc, y) {
  doc.x = MARGIN.left;
  doc.y = y;
}

/** Start a fresh page and reset the cursor. */
function newPage(doc) {
  doc.addPage();
  moveTo(doc, CONTENT_TOP);
}

/** Ensure `h` vertical space is available; otherwise add a new page. */
function ensureSpace(doc, h) {
  if (doc.y + h > CONTENT_BOTTOM) newPage(doc);
}

/** Measure the height needed to render `text` in the current font/size. */
function measure(doc, text, opts = {}) {
  return doc.heightOfString(text, { width: CONTENT_W, ...opts });
}

